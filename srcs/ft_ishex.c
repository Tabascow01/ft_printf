#include "ft_printf.h"

int		ft_ishex(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] < '0' && str[i] > '9' && ((str[i] < 'a' || str[i] < 'A')
					&& (str[i] > 'f' || str[i] > 'F')))
			return (0);
		i++;
	}
	return (1);
}
